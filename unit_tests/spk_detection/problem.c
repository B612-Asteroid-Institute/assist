// ASCII source file sample
static const char ascii_source_sample[] = "JPL Planetary EPHEMERIS DE440 ASCII source file\n";

static int create_temp_file(const char* filename, const void* data, size_t size) {
    int fd = open(filename, O_CREAT | O_WRONLY | O_TRUNC, 0644);
    if (fd < 0) return -1;
    
    if (write(fd, data, size) != (ssize_t)size) {
        close(fd);
        return -1;
    }
    
    close(fd);
    return 0;
}

int main() {
    int fd;
    ephemeris_file_format_t format;
    struct spk_s* spk;
    
    // Test valid BSP file detection
    assert(create_temp_file("test_valid.bsp", valid_bsp_sample, sizeof(valid_bsp_sample)) == 0);
    fd = open("test_valid.bsp", O_RDONLY);
    assert(fd >= 0);
    format = assist_detect_ephemeris_file_format(fd);
    close(fd);
    assert(format == FILE_FORMAT_VALID_BSP);
    unlink("test_valid.bsp");
    
    // Test legacy binary file detection
    assert(create_temp_file("test_legacy.440", legacy_binary_sample, sizeof(legacy_binary_sample)) == 0);
    fd = open("test_legacy.440", O_RDONLY);
    assert(fd >= 0);
    format = assist_detect_ephemeris_file_format(fd);
    close(fd);
    assert(format == FILE_FORMAT_BINARY_LEGACY);
    
    // Test legacy binary file rejection through ASSIST API
    spk = assist_spk_init("test_legacy.440");
    assert(spk == NULL);
    unlink("test_legacy.440");
    
    // Test ASCII source file detection
    assert(create_temp_file("test_ascii.440", ascii_source_sample, strlen(ascii_source_sample)) == 0);
    fd = open("test_ascii.440", O_RDONLY);
    assert(fd >= 0);
    format = assist_detect_ephemeris_file_format(fd);
    close(fd);
    assert(format == FILE_FORMAT_ASCII_SOURCE);
    
    // Test ASCII source file rejection through ASSIST API
    spk = assist_spk_init("test_ascii.440");
    assert(spk == NULL);
    unlink("test_ascii.440");
    
    // Test unknown file format
    const char unknown_sample[] = "Not an ephemeris file";
    assert(create_temp_file("test_unknown.dat", unknown_sample, strlen(unknown_sample)) == 0);
    fd = open("test_unknown.dat", O_RDONLY);
    assert(fd >= 0);
    format = assist_detect_ephemeris_file_format(fd);
    close(fd);
    assert(format == FILE_FORMAT_UNKNOWN);
    unlink("test_unknown.dat");
    
    // Test signature detection function directly
    assert(create_temp_file("test_sig_valid.bsp", valid_bsp_sample, sizeof(valid_bsp_sample)) == 0);
    fd = open("test_sig_valid.bsp", O_RDONLY);
    assert(assist_detect_legacy_binary_signature(fd) == 0);
    close(fd);
    unlink("test_sig_valid.bsp");
    
    assert(create_temp_file("test_sig_legacy.440", legacy_binary_sample, sizeof(legacy_binary_sample)) == 0);
    fd = open("test_sig_legacy.440", O_RDONLY);
    assert(assist_detect_legacy_binary_signature(fd) == 1);
    close(fd);
    unlink("test_sig_legacy.440");
    
    // Test with real BSP file if available
    const char* test_files[] = {"../../data/de440.bsp", "/tmp/de440.bsp", "./de440.bsp"};
    for (int i = 0; i < 3; i++) {
        fd = open(test_files[i], O_RDONLY);
        if (fd >= 0) {
            format = assist_detect_ephemeris_file_format(fd);
            close(fd);
            assert(format == FILE_FORMAT_VALID_BSP);
            
            spk = assist_spk_init(test_files[i]);
            if (spk) {
                assert(spk->num == 14);  // Expected target count
                assist_spk_free(spk);
            }
            break;
        }
    }
    
    return 0;
} 